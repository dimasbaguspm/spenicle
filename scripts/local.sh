#!/usr/bin/env bash
set -e

# Use docker-compose.dev.yml for local development
COMPOSE_FILE="docker-compose.dev.yml"
ENV_FILE=".env.dev"

# Function to print environment info
print_env_info() {
  echo "üöÄ Spenicle Local Development Environment"
  echo "üìÑ Compose file: $COMPOSE_FILE"
  echo "üîß Environment file: $ENV_FILE"
  echo "üê≥ Docker Compose command: docker compose -f $COMPOSE_FILE --env-file $ENV_FILE"
  echo ""
}

# Print environment info immediately for all commands except help
if [ "$1" != "" ] && [ "$1" != "--help" ] && [ "$1" != "-h" ]; then
  print_env_info
  
  # Validate required files exist
  if [ ! -f "$COMPOSE_FILE" ]; then
    echo "‚ùå Error: Compose file '$COMPOSE_FILE' not found!"
    exit 1
  fi
  
  if [ ! -f "$ENV_FILE" ]; then
    echo "‚ùå Error: Environment file '$ENV_FILE' not found!"
    exit 1
  fi
fi

case "$1" in
  up)
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE up -d
    ;;
  down)
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE down
    ;;
  rebuild)
    SERVICE=${2:-""}
    if [ -n "$SERVICE" ]; then
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE build --no-cache $SERVICE
    else
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE build --no-cache
    fi
    ;;
  seed)
    echo "üå± Seeding development database..."
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE exec api yarn db:seed
    ;;
  backup-now)
    echo "üóÑÔ∏è Creating immediate database backup..."
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE exec -T db-backup /scripts/backup-db-cron.sh
    ;;
  backup-status)
    echo "üìä Backup service status:"
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE ps db-backup
    echo ""
    echo "üìÅ Available backups:"
    if [ -d "backups" ]; then
      ls -la backups/spenicle_backup_*.sql.gz 2>/dev/null || echo "  No automated backups found"
    else
      echo "  Backups directory not found"
    fi
    ;;
  backup-logs)
    echo "üìã Backup service logs:"
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE logs db-backup
    ;;
  restore-backup)
    BACKUP_FILE=${2:-""}
    if [ -z "$BACKUP_FILE" ]; then
      echo "‚ùå Error: Please specify backup file"
      echo "Usage: $0 restore-backup <backup-file>"
      echo ""
      echo "Available backups:"
      ls -la backups/spenicle_backup_*.sql.gz 2>/dev/null || echo "  No automated backups found"
      exit 1
    fi
    if [ ! -f "backups/$BACKUP_FILE" ]; then
      echo "‚ùå Error: Backup file 'backups/$BACKUP_FILE' not found"
      exit 1
    fi
    
    # Source environment variables to get database credentials
    if [ -f "$ENV_FILE" ]; then
      set -a  # automatically export all variables
      source "$ENV_FILE"
      set +a
    fi
    
    echo "‚ö†Ô∏è  WARNING: This will completely replace the current database!"
    echo "üìÇ Backup file: $BACKUP_FILE"
    echo "üóÑÔ∏è  Target database: ${POSTGRES_DB:-spenicle}"
    echo ""
    read -p "Are you sure you want to continue? (y/N): " -n 1 -r
    echo
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
      echo "‚ùå Restore cancelled"
      exit 1
    fi
    
    echo "üîÑ Restoring database from backup: $BACKUP_FILE"
    
    # Step 1: Drop and recreate the database to ensure clean state
    echo "  üóëÔ∏è  Dropping existing database..."
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE exec -T postgres psql -U "${POSTGRES_USER:-postgres}" -c "DROP DATABASE IF EXISTS \"${POSTGRES_DB:-spenicle}\";"
    
    echo "  üÜï Creating fresh database..."
    docker compose -f $COMPOSE_FILE --env-file $ENV_FILE exec -T postgres psql -U "${POSTGRES_USER:-postgres}" -c "CREATE DATABASE \"${POSTGRES_DB:-spenicle}\";"
    
    # Step 2: Restore from backup
    echo "  üì• Restoring data from backup..."
    if gunzip -c "backups/$BACKUP_FILE" | docker compose -f $COMPOSE_FILE --env-file $ENV_FILE exec -T postgres psql -U "${POSTGRES_USER:-postgres}" "${POSTGRES_DB:-spenicle}" > /dev/null; then
      echo "  ‚úÖ Database restored successfully!"
      echo ""
      echo "üìä Restore summary:"
      echo "  üìÇ Source: $BACKUP_FILE"
      echo "  üóÑÔ∏è  Target: ${POSTGRES_DB:-spenicle}"
      echo "  üìÖ Completed: $(date)"
    else
      echo "  ‚ùå Database restore failed!"
      exit 1
    fi
    ;;
  logs)
    SERVICE=${2:-""}
    if [ -n "$SERVICE" ]; then
      echo "üìã Showing logs for service: $SERVICE"
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE logs -f $SERVICE
    else
      echo "üìã Showing logs for all services"
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE logs -f
    fi
    ;;
  restart)
    SERVICE=${2:-""}
    if [ -n "$SERVICE" ]; then
      echo "Restarting service: $SERVICE"
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE restart $SERVICE
    else
      echo "Restarting all services"
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE restart
    fi
    ;;
  clean-cache)
    SERVICE=${2:-""}
    if [ -n "$SERVICE" ]; then
      echo "Cleaning cache for service: $SERVICE"
      
      # Check if service is running and stop it only if needed
      if docker compose -f $COMPOSE_FILE --env-file $ENV_FILE ps --services --filter "status=running" | grep -q "^$SERVICE$"; then
        echo "  üõë Stopping running service: $SERVICE"
        docker compose -f $COMPOSE_FILE --env-file $ENV_FILE stop $SERVICE
      else
        echo "  ‚ÑπÔ∏è  Service $SERVICE is not running"
      fi
      
      # Remove the container completely
      echo "  üóëÔ∏è  Removing container for: $SERVICE"
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE rm -f -v $SERVICE
      
      # Get and remove the image
      IMAGE_NAME=$(docker compose -f $COMPOSE_FILE --env-file $ENV_FILE config | grep -A5 "$SERVICE:" | grep "image:" | awk '{print $2}')
      if [ -n "$IMAGE_NAME" ]; then
        echo "  üóëÔ∏è  Removing image: $IMAGE_NAME"
        docker rmi -f $IMAGE_NAME 2>/dev/null || echo "  ‚ö†Ô∏è  Image $IMAGE_NAME not found or already removed"
      fi
      
      # Remove associated volumes if it's postgres service
      if [ "$SERVICE" = "postgres" ]; then
        echo "  üóÑÔ∏è  Removing postgres volume..."
        docker volume rm -f "$(basename "$(pwd)")_postgres_data" 2>/dev/null || echo "  ‚ö†Ô∏è  Postgres volume not found or already removed"
      fi
      
      # Remove orphaned containers
      echo "  üßπ Removing orphaned containers..."
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE down --remove-orphans
      
      # Rebuild the service (without starting)
      echo "  üî® Rebuilding service: $SERVICE"
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE build --no-cache $SERVICE
      echo "  ‚úÖ Cache cleaned and service rebuilt for: $SERVICE"
      echo "  üí° Run './scripts/local.sh up' to start all services"
    else
      echo "‚ö†Ô∏è  WARNING: This will completely remove ALL containers, images, and volumes!"
      echo "üóÑÔ∏è  This includes the postgres database volume - all data will be lost!"
      echo ""
      read -p "Are you sure you want to continue? (y/N): " -n 1 -r
      echo
      if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        echo "‚ùå Clean cache cancelled"
        exit 1
      fi
      
      echo "Cleaning cache for all services"
      
      # Stop all services and remove volumes
      echo "  üõë Stopping all services and removing volumes..."
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE down --volumes --remove-orphans
      
      # Remove all containers forcefully
      echo "  üóëÔ∏è  Removing all containers..."
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE rm -f -v
      
      # Get all images from the compose file and remove them
      echo "  üóëÔ∏è  Removing all compose images..."
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE config --images | while read -r image; do
        if [ -n "$image" ]; then
          echo "    üóëÔ∏è  Removing image: $image"
          docker rmi -f "$image" 2>/dev/null || echo "    ‚ö†Ô∏è  Image $image not found or already removed"
        fi
      done
      
      # Remove project-specific volumes
      echo "  üóÑÔ∏è  Removing project volumes..."
      PROJECT_NAME=$(basename "$(pwd)")
      docker volume ls -q --filter "name=${PROJECT_NAME}_" | while read -r volume; do
        if [ -n "$volume" ]; then
          echo "    üóëÔ∏è  Removing volume: $volume"
          docker volume rm -f "$volume" 2>/dev/null || echo "    ‚ö†Ô∏è  Volume $volume not found or already removed"
        fi
      done
      
      # Remove dangling images and build cache
      echo "  üßπ Removing dangling images and build cache..."
      docker image prune -f
      docker builder prune -f
      docker volume prune -f
      
      # Rebuild all services (without starting)
      echo "  üî® Rebuilding all services..."
      docker compose -f $COMPOSE_FILE --env-file $ENV_FILE build --no-cache
      echo "  ‚úÖ Cache cleaned and all services rebuilt"
      echo "  üí° Run './scripts/local.sh up' to start all services"
    fi
    ;;
  check)
    echo "üîç Pre-deployment checks (Development):"
    echo ""
    
    # Check if required files exist
    echo "üìÅ Checking required files..."
    for file in "$COMPOSE_FILE" "$ENV_FILE"; do
      if [ -f "$file" ]; then
        echo "  ‚úÖ $file ($(du -h "$file" | cut -f1))"
      else
        echo "  ‚ùå $file (missing)"
      fi
    done
    echo ""
    
    # Check environment variables
    echo "üåç Checking environment variables..."
    if [ -f "$ENV_FILE" ]; then
      echo "  ‚úÖ Environment file found"
      echo "  üìã Key variables:"
      grep -E "^(NODE_ENV|API_|WEB_|POSTGRES_)" "$ENV_FILE" | head -5 | while read line; do
        echo "    $line"
      done
    else
      echo "  ‚ùå Environment file missing"
    fi
    
    # Check Docker
    echo ""
    echo "üê≥ Checking Docker..."
    if command -v docker >/dev/null 2>&1; then
      echo "  ‚úÖ Docker is installed"
      if docker info >/dev/null 2>&1; then
        echo "  ‚úÖ Docker daemon is running"
      else
        echo "  ‚ùå Docker daemon is not running"
      fi
    else
      echo "  ‚ùå Docker is not installed"
    fi
    ;;
  generate-config)
    echo "‚ÑπÔ∏è  Note: Config generation is typically not needed for local development."
    echo "üîß For development, services usually run without nginx proxy."
    echo ""
    echo "If you need to test production-like setup locally, consider:"
    echo "  1. Using './scripts/prod.sh generate-config' instead"
    echo "  2. Or running services individually without Docker Compose"
    ;;
  *)
    echo "Usage: $0 {up|down|rebuild [service]|seed|backup-now|backup-status|backup-logs|restore-backup <file>|logs [service]|restart [service]|clean-cache [service]|check|generate-config}"
    echo ""
    echo "Commands:"
    echo "  up                      - Start all services"
    echo "  down                    - Stop all services"
    echo "  rebuild [service]       - Rebuild and restart service(s)"
    echo "  seed                    - Seed development database with fake data"
    echo "  backup-now             - Trigger immediate automated backup"
    echo "  backup-status          - Show backup service status and available backups"
    echo "  backup-logs            - Show backup service logs"
    echo "  restore-backup <file>  - Restore database from automated backup"
    echo "  logs [service]         - Show service logs (all services if no service specified)"
    echo "  restart [service]      - Restart service(s)"
    echo "  clean-cache [service]  - Clean Docker cache, remove containers/images/volumes and rebuild"
    echo "  check                  - Run pre-deployment checks"
    echo "  generate-config        - Info about config generation (dev mode)"
    echo ""
    print_env_info
    exit 1
    ;;
esac
